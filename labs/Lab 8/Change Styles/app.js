// create function that will change the style of the div clicked
function changeStyle(divId) {

    //change the background color as blue when button is clicked 
    document.getElementById(divId).style.backgroundColor = "blue";

    //Change the width to make it smaller when button is clicked 
    document.getElementById(divId).style.width = "200px";
}